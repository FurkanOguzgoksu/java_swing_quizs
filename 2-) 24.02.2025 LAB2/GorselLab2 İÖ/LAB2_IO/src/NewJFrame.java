        String meyveler;
        
        meyveler = jComboBox1.getSelectedItem().toString();
        
        if (jRadioButton3.isSelected()) 
            meyveler = meyveler + ", " + jRadioButton3.getText();
            //meyveler += "Çikolata, ";
        else if (jRadioButton4.isSelected()) 
            meyveler = meyveler + ", " + jRadioButton4.getText();
            //meyveler += "Karamel, ";
        
