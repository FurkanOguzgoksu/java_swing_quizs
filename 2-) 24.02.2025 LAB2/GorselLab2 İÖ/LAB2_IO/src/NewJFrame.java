            meyveler = meyveler + ", " + jCheckBox1.getText();
            //meyveler += "Çilek, ";
        if (jCheckBox2.isSelected())             
            meyveler = meyveler + ", " + jCheckBox2.getText();
            //meyveler += "Muz, ";
        if (jCheckBox3.isSelected())             
            meyveler = meyveler + ", " + jCheckBox3.getText();
            //meyveler += "Kivi, ";
        if (jCheckBox4.isSelected()) 
            meyveler = meyveler + ", " + jCheckBox4.getText();
            //meyveler += "Frambuaz, ";
        
        dlm.addElement(meyveler);
        jList1.setEnabled(false);
        
        jLabel1.setForeground(Color.red);
        if (jRadioButton1.isSelected())
            jLabel1.setText("Kapıda nakit ödeme ile sipariş tamamlanmıştır. Teşekkür ederiz.");
        if (jRadioButton2.isSelected())
            jLabel1.setText("Kredi kartı ödemesi ile sipariş tamamlanmıştır. Teşekkür ederiz.");       
